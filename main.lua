function drawdigit(t, x, y)
  x = x or 0
  y = y or 0
  for i,v in ipairs(t) do
    -- 1: index, 2: fx, 3: fy
    fx=false
    fy=false
    if(sub(v,2,2) == "1") then fx=true end
    if(sub(v,3,3) == "1") then fy=true end

    spr(sub(v,1,1), x+(i-1)%2*8, y+(flr((i+1)/2)-1)*8, 1, 1, fx, fy)
  end
end

